# Rules to create a variable in Python
# 1. Variable name should start with a letter or an underscore(_)
# 2. Variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ )
# 3. Variable name are case-sensitive (name, Name and NAME are three different variables)
# 4. Variable name should not be a reserved word (like print, for, if, else, etc.)
# 6. Variable name should not start with a number
# 7. Variable name should not contain special characters (!, @, #, $, %, ^, &, *, etc.)
# 8. Variable name should not contain spaces. Use underscore(_) instead of space

